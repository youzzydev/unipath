export function getUniversityIntelligence(slug: string): UniversityIntelligence | null {
  return universityIntelligenceData[slug] || null;
}

export function getTopRecommendedUniversities(limit: number = 10): string[] {
  return Object.entries(universityIntelligenceData)
    .sort((a, b) => b[1].fitScore - a[1].fitScore)
    .slice(0, limit)
    .map(([slug]) => slug);
}

export function getFitScoreColor(score: number): string {
  if (score >= 85) return 'text-[#00FF88]';
  if (score >= 70) return 'text-[#00D9FF]';
  if (score >= 55) return 'text-[#FFB800]';
  return 'text-[#FF4757]';
}

